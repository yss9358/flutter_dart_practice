void main(){
  // 제어문 switch case

  dynamic no = '4';


  switch(no){
    case '1' :
      print('101');
      break;
    case '2' :
      print('202');
      break;
    case '3' :
      print('303');
      break;
    case '4' :
      print('404');
      break;
    default :
      print('상담원');
      break;
  }

}
